str = 'Runnoob'
print(str)              # 输出字符串
print(str[0:-1])        # 输出第一个到倒数第二个的所有字符串
print(str[0])           # 输出字符串第一个字符
print(str[2:5])         # 输出从第三个字符串开始到第五个的字符
print(str[2:])          # 输出从第三个开始的后面的所有字符
print(str * 2)          # 输出从第三个开始的所有的字符
print(str + 'TEST')     # 连接字符串
