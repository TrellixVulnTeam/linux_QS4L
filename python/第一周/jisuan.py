a = 20
b = 10
# 打印a和b
print("a = ",a,'\n'
      "b = ",b)
# 加法: +
print(a,'+',b,' = ',a + b,';')
# 减法: -
print(a,'-',b,' = ',a - b,';')
# 乘法: *
print(a,'*',b,' = ',a * b,';')
# 除法: /
print(a,'/',b,' = ',a / b,';')
# 取模，返回除法的余数: %
print(a,'%',b,' = ',a % b,';')
# 幂：**
print(a,'**',b,' =  ',a ** b,';')
# 取整数，返回除法商的整数：//
print(a,'//',b,' = ',a // b,';')
